package com.persistent.models;

public class Organization {
	
	public String createdat;
	public String defaultVal;
	public String details;
	public String externalid;
	public String groupid;
	public String id;
	public String isshared;
	public String issharedcomments;
	public String name;
	public String notes;
	public String suspended;
	public String updatedat;
	public String currenttags;
	
	public String getCreatedat() {
		return createdat;
	}
	public void setCreatedat(String createdat) {
		this.createdat = createdat;
	}
	public String getDefaultVal() {
		return defaultVal;
	}
	public void setDefaultVal(String defaultVal) {
		this.defaultVal = defaultVal;
	}
	public String getDetails() {
		return details;
	}
	public void setDetails(String details) {
		this.details = details;
	}
	public String getExternalid() {
		return externalid;
	}
	public void setExternalid(String externalid) {
		this.externalid = externalid;
	}
	public String getGroupid() {
		return groupid;
	}
	public void setGroupid(String groupid) {
		this.groupid = groupid;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIsshared() {
		return isshared;
	}
	public void setIsshared(String isshared) {
		this.isshared = isshared;
	}
	public String getIssharedcomments() {
		return issharedcomments;
	}
	public void setIssharedcomments(String issharedcomments) {
		this.issharedcomments = issharedcomments;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNotes() {
		return notes;
	}
	public void setNotes(String notes) {
		this.notes = notes;
	}
	public String getSuspended() {
		return suspended;
	}
	public void setSuspended(String suspended) {
		this.suspended = suspended;
	}
	public String getUpdatedat() {
		return updatedat;
	}
	public void setUpdatedat(String updatedat) {
		this.updatedat = updatedat;
	}
	public String getCurrenttags() {
		return currenttags;
	}
	public void setCurrenttags(String currenttags) {
		this.currenttags = currenttags;
	}
}
